package delivery.model;

/**
 * Класс для представления точки на двумерной плоскости.
 * 
 * Используется для хранения координат объектов в системе доставки:
 * - Позиции транспортных единиц (сортировочный центр, фургон, курьер)
 * - Точки назначения посылок
 * - Промежуточных точек маршрута
 * 
 * Предоставляет методы для работы с координатами и вычисления расстояний.
 */
public class Point {
    //Координаты X и Y точки на плоскости.
    private double x;
    private double y;

    //Создает новую точку с указанными координатами.
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    //Возвращает координату X точки.
    public double getX() {
        return x;
    }

    //Возвращает координату Y точки.
    public double getY() {
        return y;
    }

    /**
     * Вычисляет евклидово расстояние от текущей точки до другой точки.
     * 
     * Использует формулу: sqrt((x2-x1)² + (y2-y1)²)
     * 
     * other - другая точка, до которой вычисляется расстояние
     * return - расстояние между точками
     */
    public double distanceTo(Point other) {
        // Вычисляем разности координат
        double dx = this.x - other.x;
        double dy = this.y - other.y;
        // Вычисляем евклидово расстояние по теореме Пифагора
        return Math.sqrt(dx * dx + dy * dy);
    }
}
